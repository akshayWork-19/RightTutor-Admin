export const getStatusStyles = (status: Status) => {
  // Using opacity-based colors for perfect theme integration (Light & Dark)
  switch (status) {
    case Status.RESOLVED:
    case Status.COMPLETED:
    case Status.ASSIGNED:
      return 'bg-emerald-500/10 text-emerald-500 border border-emerald-500/20';
    case Status.PENDING:
    case Status.SCHEDULED:
    case Status.TRIAL_PENDING:
    case Status.PROCESSING:
      return 'bg-amber-500/10 text-amber-500 border border-amber-500/20';
    case Status.CANCELLED:
    case Status.DROPPED:
      return 'bg-rose-500/10 text-rose-500 border border-rose-500/20';
    default:
      return 'bg-neutral-500/10 text-neutral-500 border border-neutral-500/20';
  }
};

